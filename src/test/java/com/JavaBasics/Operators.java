package com.JavaBasics;

public class Operators {
	public static void main(String[] args) {
		int i =7,j=8;
		System.out.println(i++);
		System.out.println(++i);
		System.out.println(i--);
		System.out.println(--i);
		
		System.out.println(++j + j++);
		System.out.println(j++ + ++j);
		System.out.println(--j - j--);
		System.out.println(j-- - j++);
	}
}
